//! Extract the on-chain verifying key from a finalized `CeremonyState`
//! (ADR-017 stage 2) — the last mile of the public ceremony.
//!
//! Reads the frozen ceremony state (`final.state`, or a path given as arg),
//! self-tests that its proving key encodes a working `WithdrawCircuit<20>`
//! (proves + verifies a real withdraw), converts the VK to the groth16-solana
//! byte layout, and rewrites `programs/tidex6-verifier/src/withdraw_vk.rs`.
//!
//! The output is byte-deterministic from the frozen state: anyone who downloaded
//! the final `CeremonyState` can reproduce the exact VK that goes on-chain, then
//! diff it against the deployed verifier.
//!
//! Usage:
//!
//! ```text
//! cargo run --bin ceremony_extract_vk --release [path/to/final.state]
//! ```

use std::fs;

use tidex6_circuits::ceremony::{find_workspace_root, render_vk_source, selftest_pk};
use tidex6_circuits::mpc::CeremonyState;
use tidex6_circuits::solana_bytes::{Groth16SolanaBytes, groth16_to_solana_bytes};

/// Fixed self-test prover seed — this is only a functional check of the setup,
/// not a production proof, so a fixed seed is fine (and makes the run reproducible).
const SELFTEST_SEED: u64 = 0xce12_0517_beac_0f17;

/// Header comment for the ceremony-derived `withdraw_vk.rs` — records the
/// multi-party origin (contrast with the DEVELOPMENT-ONLY dev generator).
const CEREMONY_HEADER: &str = r#"//! Hardcoded verifying key for the production `WithdrawCircuit<20>`.
//!
//! **DO NOT EDIT BY HAND.** Generated by
//! `crates/tidex6-circuits/src/bin/ceremony_extract_vk.rs` from the FINALIZED
//! public trusted-setup ceremony state (`~/.tidex6-ceremony/final.state`,
//! ADR-017). Byte-reproducible: anyone who downloaded the final `CeremonyState`
//! can re-run the extract and diff this file.
//!
//! Multi-party Phase-2 setup — secure as long as at least one contributor was
//! honest and destroyed their randomness (1-of-N). The final contribution is a
//! public drand beacon (see `final.json`).
"#;

fn main() {
    let home = std::env::var("HOME").expect("HOME");
    let default_path = format!("{home}/.tidex6-ceremony/final.state");
    let state_path = std::env::args().nth(1).unwrap_or(default_path);
    println!("reading ceremony state: {state_path}");

    let state = CeremonyState::from_bytes(&fs::read(&state_path).expect("read ceremony state"))
        .expect("parse CeremonyState");
    println!("contributions in state: {}", state.contributions.len());
    for c in &state.contributions {
        println!("  - {}", c.name);
    }

    // Self-test: prove + verify a real withdraw with this proving key. Fails hard
    // if the setup is broken; returns the VK we extract.
    println!("self-testing the proving key (prove + verify a withdraw)…");
    let vk = selftest_pk(&state.pk, SELFTEST_SEED).expect("ceremony pk self-test failed");
    println!("self-test OK — setup encodes a working WithdrawCircuit<20>");

    // `groth16_to_solana_bytes` needs a proof argument (to negate `proof_a`); we
    // only care about VK fields, so pass a throwaway proof.
    let dummy_proof = ark_groth16::Proof::<ark_bn254::Bn254> {
        a: vk.alpha_g1,
        b: vk.beta_g2,
        c: vk.alpha_g1,
    };
    let bytes: Groth16SolanaBytes =
        groth16_to_solana_bytes(&dummy_proof, &vk).expect("groth16_to_solana_bytes");
    println!("  vk_ic length    = {}", bytes.vk_ic.len());
    println!("  nr_public_input = {}", bytes.vk_ic.len() - 1);

    let out_path =
        find_workspace_root().join("programs/tidex6-verifier/src/withdraw_vk.rs");
    let source = render_vk_source(&bytes, CEREMONY_HEADER);
    if let Some(parent) = out_path.parent() {
        fs::create_dir_all(parent).expect("create verifier src dir");
    }
    fs::write(&out_path, source).expect("write withdraw_vk.rs");
    println!("wrote {}", out_path.display());

    let fmt = std::process::Command::new("rustfmt")
        .arg(&out_path)
        .status()
        .expect("spawn rustfmt; is it on PATH?");
    if !fmt.success() {
        panic!("rustfmt failed on {}", out_path.display());
    }
    println!("formatted via rustfmt");
    println!("done — deploy a fresh immutable verifier carrying this VK (ADR-017 stage 4)");
}
