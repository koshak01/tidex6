//! Генератор verifying-key для схем скрытой суммы (withdraw + transfer).
//!
//! Детерминированный single-contributor Groth16 Phase-2 setup с фиксированным
//! seed'ом — регенерируется бит-в-бит на любой машине. Для каждой схемы пишет:
//!
//! 1. `artifacts/<name>_vk.rs` — `Groth16Verifyingkey`-константа в байтовой
//!    раскладке `groth16-solana`, готовая к `include!` в on-chain-программу.
//! 2. `artifacts/<name>_pk.bin` — сериализованный `ProvingKey<Bn254>` для
//!    prover-стороны (клиент / CLI / wasm).
//!
//! **DEVELOPMENT ONLY.** Single-contributor: toxic waste существует на одной
//! машине. Перед mainnet — заменить multi-party Phase-2 церемонией
//! (`ceremony.tidex6.com`). См. `docs/release/security.md`.
//!
//! Запуск: `cargo run -p tidex6-confidential --bin gen_vk --release`

use std::fs;
use std::io::Write;
use std::path::{Path, PathBuf};

use ark_bn254::Bn254;
use ark_groth16::{ProvingKey, VerifyingKey};
use ark_serialize::CanonicalSerialize;
use ark_std::rand::rngs::StdRng;
use ark_std::rand::SeedableRng;
use tidex6_circuits::solana_bytes::{groth16_to_solana_bytes, Groth16SolanaBytes};
use tidex6_confidential::{transfer, withdraw};

/// Fixed seeds (ASCII-мнемоника). Смена seed / схемы / arkworks-версии
/// инвалидирует VK и требует редеплоя верификатора.
const WITHDRAW_SEED: u64 = 0x775f_6869_6464_656e; // "w_hidden"
const TRANSFER_SEED: u64 = 0x745f_6869_6464_656e; // "t_hidden"

fn main() {
    let artifacts = find_workspace_root().join("crates/tidex6-confidential/artifacts");
    fs::create_dir_all(&artifacts).expect("create artifacts dir");

    // ── withdraw ─────────────────────────────────────────────────
    let mut rng = StdRng::seed_from_u64(WITHDRAW_SEED);
    let (pk, vk) = withdraw::setup(&mut rng).expect("withdraw setup");
    emit(
        &artifacts,
        "withdraw",
        "WITHDRAW",
        withdraw::WITHDRAW_NR_PUBLIC_INPUTS,
        &pk,
        &vk,
    );

    // ── transfer ─────────────────────────────────────────────────
    let mut rng = StdRng::seed_from_u64(TRANSFER_SEED);
    let (pk, vk) = transfer::setup(&mut rng).expect("transfer setup");
    emit(
        &artifacts,
        "transfer",
        "TRANSFER",
        transfer::TRANSFER_NR_PUBLIC_INPUTS,
        &pk,
        &vk,
    );

    println!("Done.");
}

/// Конвертирует VK в groth16-solana байты, пишет `<name>_vk.rs` и `<name>_pk.bin`.
fn emit(
    dir: &Path,
    name: &str,
    prefix: &str,
    expected_public_inputs: usize,
    pk: &ProvingKey<Bn254>,
    vk: &VerifyingKey<Bn254>,
) {
    // `groth16_to_solana_bytes` требует proof только чтобы негировать proof_a;
    // нам нужны лишь VK-поля, поэтому подсовываем dummy proof из полей VK.
    let dummy_proof = ark_groth16::Proof::<Bn254> {
        a: vk.alpha_g1,
        b: vk.beta_g2,
        c: vk.alpha_g1,
    };
    let bytes = groth16_to_solana_bytes(&dummy_proof, vk).expect("groth16_to_solana_bytes");
    let nr_public = bytes.vk_ic.len() - 1;
    assert_eq!(
        nr_public, expected_public_inputs,
        "{name}: VK IC подразумевает {nr_public} публичных входов, схема заявляет {expected_public_inputs}"
    );

    let vk_path = dir.join(format!("{name}_vk.rs"));
    fs::write(&vk_path, render_vk_source(prefix, name, &bytes)).expect("write vk file");
    let _ = std::process::Command::new("rustfmt").arg(&vk_path).status();
    println!("Wrote {} ({} public inputs)", vk_path.display(), nr_public);

    let pk_path = dir.join(format!("{name}_pk.bin"));
    let mut pk_bytes = Vec::new();
    pk.serialize_uncompressed(&mut pk_bytes)
        .expect("serialize pk");
    fs::File::create(&pk_path)
        .expect("create pk file")
        .write_all(&pk_bytes)
        .expect("write pk");
    println!("Wrote {} ({} bytes)", pk_path.display(), pk_bytes.len());
}

/// Рендерит `<name>_vk.rs` с `Groth16Verifyingkey`-константой.
fn render_vk_source(prefix: &str, name: &str, bytes: &Groth16SolanaBytes) -> String {
    let nr_public = bytes.vk_ic.len() - 1;
    let mut out = String::new();
    out.push_str(&format!(
        "//! Hardcoded verifying key for the confidential `{name}` circuit.\n\
         //!\n\
         //! **DO NOT EDIT BY HAND.** Generated by\n\
         //! `crates/tidex6-confidential/src/bin/gen_vk.rs` with a fixed seed.\n\
         //! **DEVELOPMENT ONLY** — single-contributor setup; replace with a\n\
         //! multi-party ceremony before mainnet.\n\n\
         use groth16_solana::groth16::Groth16Verifyingkey;\n\n\
         pub const {prefix}_NR_PUBLIC_INPUTS: usize = {nr_public};\n\n"
    ));

    out.push_str(&format!(
        "#[allow(clippy::type_complexity)]\n\
         static {prefix}_VK_IC: [[u8; 64]; {}] = [\n",
        bytes.vk_ic.len()
    ));
    for point in &bytes.vk_ic {
        out.push_str(&render_byte_array(point));
        out.push_str(",\n");
    }
    out.push_str("];\n\n");

    for (label, len, data) in [
        ("ALPHA_G1", 64, &bytes.vk_alpha_g1[..]),
        ("BETA_G2", 128, &bytes.vk_beta_g2[..]),
        ("GAMMA_G2", 128, &bytes.vk_gamma_g2[..]),
        ("DELTA_G2", 128, &bytes.vk_delta_g2[..]),
    ] {
        out.push_str(&format!("static {prefix}_VK_{label}: [u8; {len}] = "));
        out.push_str(&render_byte_array(data));
        out.push_str(";\n\n");
    }

    out.push_str(&format!(
        "pub const {prefix}_VERIFYING_KEY: Groth16Verifyingkey = Groth16Verifyingkey {{\n\
         \x20   nr_pubinputs: {nr_public},\n\
         \x20   vk_alpha_g1: {prefix}_VK_ALPHA_G1,\n\
         \x20   vk_beta_g2:  {prefix}_VK_BETA_G2,\n\
         \x20   vk_gamme_g2: {prefix}_VK_GAMMA_G2,\n\
         \x20   vk_delta_g2: {prefix}_VK_DELTA_G2,\n\
         \x20   vk_ic:       &{prefix}_VK_IC,\n\
         }};\n"
    ));
    out
}

/// Рендерит байтовый срез как Rust-массив-литерал.
fn render_byte_array(bytes: &[u8]) -> String {
    let mut out = String::from("[\n");
    for chunk in bytes.chunks(12) {
        out.push_str("    ");
        for b in chunk {
            out.push_str(&format!("0x{b:02x}, "));
        }
        out.push('\n');
    }
    out.push(']');
    out
}

/// Находит корень workspace, поднимаясь от текущего исполняемого крейта.
fn find_workspace_root() -> PathBuf {
    let mut dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    while !dir.join("Cargo.lock").exists() {
        if !dir.pop() {
            panic!("workspace root not found");
        }
    }
    dir
}
