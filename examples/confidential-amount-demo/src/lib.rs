//! Confidential Amount Demo — песочница для понимания того, как
//! выглядит приватная передача суммы через Pedersen commitment'ы.
//!
//! **Это не часть tidex6 MVP.** Отдельный крейт, не связанный с
//! верификатором, индексером или client-SDK. Сюда мы складываем
//! эксперименты по прятанию суммы — чтобы посмотреть как это
//! выглядит и решить, что из этого стоит вводить в протокол.
//!
//! # Что показывает демо
//!
//! 1. Каждый пользователь имеет **баланс, зашифрованный как
//!    Pedersen commitment**: на chain виден кусок байтов, сумму
//!    «с улицы» вычислить невозможно.
//! 2. Перевод работает **гомоморфно**: mult commitment отправителя
//!    на обратный коммит «минус amount», а получателя — на прямой.
//!    Наблюдатель видит смену 32 байт → 32 байт, сколько ушло —
//!    неизвестно.
//! 3. Сам пользователь знает свой blinding factor и amount — может
//!    проверить баланс, сверить что arithmetic прошла честно.
//!
//! # Что демо НЕ делает
//!
//! - Нет range proof. Пользователь теоретически может перевести
//!   «отрицательный amount» (= украсть у получателя). В продакшене
//!   обязателен Bulletproofs или Groth16-гадгет; в учебном демо
//!   пропущено ради ясности.
//! - Нет onchain — state хранится в локальном JSON файле.
//! - Нет authentication — любой может инициировать «перевод от
//!   Alice» потому что CLI trust'ит файл.
//!
//! Обе дыры закрываются тривиально (для production), но здесь
//! важно увидеть **ядро идеи** без шума.

